public class Main {
    public static void main(String[] args){
        // expression = operands & operators
        // operands = values, variables, numbers, quantity
        // operators = +, - , /, *, %

        int friends = 10;
        friends = friends +10;
        System.out.println("Add: "+ friends);

        friends = friends - 5;
        System.out.println("Minus: "+friends);

        friends = friends *2;
        System.out.println("Multiple: "+friends);

        friends = friends /3;
        System.out.println("Division: "+ friends);

        friends = friends%4;
        System.out.println("Remandir: "+ friends);

    }
}